#include <stdio.h>
int main(int argc, char const *argv[]) {
    int a, b, c,x,y;

    scanf("%d", &a);
    scanf("%d", &b);
    scanf("%d", &c);
    scanf("%d", &x);
    scanf("%d", &y);

    printf("%d%d%d\n", a, b, c);
    printf("%d;%d\n", x, y);
    printf("%d,%d,%d\n", a, b, c);

    return 0;
}
